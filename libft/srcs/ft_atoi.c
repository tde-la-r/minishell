#include "libft.h"

int	ft_atoi(const char *str)
{
	char	*s;
	int		d;
	int		sign;
	int		i;

	s = (char *) str;
	i = 0;
	sign = 1;
	d = 0;
	while (s[i] == ' ' || s[i] == '\t' || s[i] == '\v' || \
	s[i] == '\f' || s[i] == '\r' || s[i] == '\n')
		i++;
	if (s[i] == '+' || s[i] == '-')
	{
		if (s[i] == '-')
			sign = sign * -1;
		i++;
	}
	while (s[i] >= '0' && s[i] <= '9')
	{
		d = 10 * d + (s[i] - '0');
		i++;
	}
	return (sign * d);
}
